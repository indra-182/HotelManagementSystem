    private static String getSecurityQuestion(String email) {
        String securityQuestion = "";
        String query = "SELECT security_question FROM users WHERE email = '" + email + "'";
        ResultSet resultSet = Query.getData(query);
        try {
            if (resultSet.next()) {
                securityQuestion = resultSet.getString("security_question");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return securityQuestion;
    }

    private static boolean validateAllData(Users users) {
        String query = "SELECT * FROM users WHERE email = '" + users.getEmail() + "' AND security_question = '" + users.getSecurityQuestion() + "' AND answer = '" + users.getAnswer() + "'";
        ResultSet resultSet = Query.getData(query);
        try {
            if (resultSet.next()) {
                return true;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    }


