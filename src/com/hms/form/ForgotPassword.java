    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Users users = new Users();
        users.setEmail(txtSetEmail.getText());
        users.setSecurityQuestion(txtSetSecurityQuestion.getText());
        users.setAnswer(txtSetAnswer.getText());
        users.setPassword(String.valueOf(txtSetPassword.getPassword()));

        if (users.getEmail().isEmpty() || users.getSecurityQuestion().isEmpty() || users.getAnswer().isEmpty() || users.getPassword().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua kolom harus diisi");
            return;
        }

        if (!users.getPassword().equals(String.valueOf(txtSetPasswordAgain.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Password tidak sama");
            return;
        }

        if (!validateAllData(users)) {
            JOptionPane.showMessageDialog(null, "Jawaban Anda salah");
            return;
        }

        if (validateAllData(users) && users.getPassword().equals(String.valueOf(txtSetPasswordAgain.getPassword()))) {
            String query = "UPDATE users SET password = '" + users.getPassword() + "' WHERE email = '" + users.getEmail() + "'";
            Query.setData(query, "Sukses Memperbarui Password");
            setVisible(false);
            new ForgotPassword().setVisible(true);
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        Users users = new Users();
        users.setEmail(txtSetEmail.getText());

        if (users.getEmail().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Email harus diisi");
            return;
        }

        if (!Register.validateEmail(users.getEmail())) {
            JOptionPane.showMessageDialog(null, "Email tidak ditemukan");
            return;
        }

        if (Register.validateEmail(users.getEmail())) {
            JOptionPane.showMessageDialog(null, "Email ditemukan");
            txtSetSecurityQuestion.setText(getSecurityQuestion(users.getEmail()));
            txtSetEmail.setEditable(false);
            txtSetSecurityQuestion.setEditable(false);
            return;
        }
    }//GEN-LAST:event_btnCariActionPerformed

