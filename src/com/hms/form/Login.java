    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        Users users = new Users();
        users.setEmail(txtSetEmail.getText());
        String encryptedPassword = Register.encryptPass(String.valueOf(txtSetPassword.getPassword()));
        users.setPassword(encryptedPassword);

        if (users.getEmail().isEmpty() || users.getPassword().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Email dan Password harus diisi");
            return;
        }

        if (!Register.validateEmail(users.getEmail())) {
            JOptionPane.showMessageDialog(null, "Email tidak ditemukan");
            return;
        }

        if (!validateLogin(users.getEmail(), users.getPassword())) {
            JOptionPane.showMessageDialog(null, "Email atau Password salah");
            return;
        }

        if (validateLogin(users.getEmail(), users.getPassword())) {
            if (users.getEmail().equals("superadmin@mailinator.com")) {
                setVisible(false);
                new SuperAdminHome().setVisible(true);
            } else {
                ResultSet resultSet = Query.getData("SELECT * FROM users WHERE email = '" + users.getEmail() + "' AND password = '" + users.getPassword() + "'");
                try {
                    if (resultSet.next()) {
                        if (resultSet.getString("status").equals("Active")) {
                            setVisible(false);
                            new AdminHome().setVisible(true);
                        } else {
                            JOptionPane.showMessageDialog(null, "Akun anda belum aktif, silakan hubungi Super Admin");
                        }
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

